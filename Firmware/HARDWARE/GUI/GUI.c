/**
  * @brief 实现滑动效果
  * @param  *a			当前坐标
  * @param	*a_trg		目标坐标
  * @param 	step		运动速度
  * @param	slow_cnt 	减速
  * @retval 
  */
uint8_t ui_run(short *a, short *a_trg, u8 step, u8 slow_cnt) //ui 滑动效果实现
{
	u8 temp;

	temp = abs(*a_trg - *a) > slow_cnt ? step: 1;

	if (*a < *a_trg)
	{
		*a += temp;
	}
	else if ( *a > *a_trg)
	{
		*a -= temp;
	}
	else
	{
		return 0;
	}
	return 1;
}


